package NivelMedio;

// Interface que representa habilidades únicas de quem tem sangue Hyuga E Uzumaki.
// Diferente de herança (extends), uma classe pode implementar VÁRIAS interfaces.
// Isso é uma forma de simular "herança múltipla" em Java.
public interface HyugaUzumaki {

    // Habilidade herdada do clã Uzumaki (via Karma de Otsutsuki)
    void ativarOKarma();

    // Habilidade dos olhos únicos do Boruto (mistura de Byakugan + chakra Uzumaki)
    void ativarJougan();
}
